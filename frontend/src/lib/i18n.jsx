const dictionaries = { en, sw, am };

const LocaleContext = createContext(null);

const interpolate = (value, vars = {}) =>
  Object.entries(vars).reduce(
    (message, [key, replacement]) => message.replaceAll(`{${key}}`, replacement ?? ''),
    value
  );

export function LocaleProvider({ children }) {
  const [locale, setLocale] = useState(() => window.localStorage.getItem('admin-locale') || 'en');

  useEffect(() => {
    window.localStorage.setItem('admin-locale', locale);
    document.documentElement.lang = locale;
    document.title = dictionaries[locale]?.app_title || en.app_title;
  }, [locale]);

  const value = useMemo(
    () => ({
      locale,
      setLocale,
      t: (key, vars = {}) => interpolate(dictionaries[locale]?.[key] || en[key] || key, vars),
    }),
    [locale]
  );

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export const useLocale = () => {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error('useLocale must be used within LocaleProvider.');
  }

  return context;
};
